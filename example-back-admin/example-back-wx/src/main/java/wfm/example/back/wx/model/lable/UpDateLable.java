package wfm.example.back.wx.model.lable;

public class UpDateLable {

    /**
     * 标签Id
     */
    private String id;

    /**
     * 修改后的标签名
     */
    private String name;


    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

}
